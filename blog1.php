          <h2>| Piel Normal</h2>
          <p>La piel normal es el tipo de piel más equilibrado y es el menos problemático de todos los tipos de piel.
            Tiene una textura suave y flexible con una cantidad adecuada de humedad y aceites naturales.
            Las personas con piel normal no experimentan una excesiva producción de grasa o sequedad.</p>

          <h2>| Piel Grasa</h2>
          <p>La piel grasa tiene una textura gruesa y brillante, con poros grandes y una apariencia oleosa.
            Es el resultado de una producción excesiva de aceite natural en la piel, lo que puede ser causado
            por factores como la genética, las hormonas y el uso de ciertos productos de cuidado de la piel.
            Las personas con piel grasa suelen experimentar brotes de acné y puntos negros.</p>

          <h2>| Piel Seca</h2>
          <p>La piel seca tiene una textura áspera y escamosa, con una apariencia mate y sin brillo.
            Es el resultado de una producción insuficiente de aceites naturales en la piel, lo que
            puede ser causado por factores como el clima, la edad y el uso excesivo de productos de
            limpieza fuertes. Las personas con piel seca suelen experimentar picazón, descamación y sensación de tensión en la piel.</p>

          <h2>| Piel Sensible</h2>
          <p>La piel sensible es fácilmente irritada por ciertos productos de cuidado de la piel y factores ambientales,
            como el viento y el sol. Tiene una textura fina y delicada, con una apariencia rosada o enrojecida.
            Las personas con piel sensible suelen experimentar picazón, quemazón y sequedad en la piel.</p>

          <h2>| Piel Mixta</h2>
          <p>Parecida a la grasa, pero su zona grasienta se situa en la zona T de la cara, en la frente, nariz y mentón. El resto es piel normal o seca.</p>

          <h2>| Piel Madura</h2>
          <p>La piel no tiene brillo natural, líneas de expresión o arrugas, sequedad y manchas en la piel.</p>

